// Hàm merge dữ liệu mở rộng vào nguyên tố
export function enrichElement(element) {
  const extra = elementEnrichment[element.number];
  if (!extra) return element;
  return { 
    ...element, 
    ...extra,
    // Ensure description from enrichment is used as the primary 'desc' for the UI
    desc: extra.description || element.desc,
    funFact: extra.facts ? extra.facts[0] : element.funFact
  };
}
